def add(a,b):
    """
    Add function:
    
    Inputs:
    a: number
    b: number
    
    Outputs:
    a + b 
    """
    return a + b

def mul(a,b):
    """
    Mul function:
    
    Inputs:
    a: number
    b: number
    
    Outputs:
    a * b 
    """
    return a * b    

def sub(a,b):
    """
    Sub function:
    
    Inputs:
    a: number
    b: number
    
    Outputs:
    a - b 
    """    
    return a - b 

def exp(a,b):
    """
    Exp function:
    
    Inputs:
    a: number
    b: number
    
    Outputs:
    a ** b 
    """    
    return a**b 